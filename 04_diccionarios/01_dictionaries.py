
#Diccionarios = objetos (JSON) 
#Colecciones de datos desordenadas, mutables e indexadas.
#Permiten almacenar datos en pares clave:valor
#Se definen con llaves {}
#Cada elemento tiene una clave y un valor asociado
#Las claves deben ser únicas y de un tipo inmutable (cadenas, números o tuplas)
#Los valores pueden ser de cualquier tipo de dato
#Sintaxis: mi_diccionario = {clave1: valor1, clave2: valor2, ...}
#Crear un diccionario
mi_diccionario = {
    "nombre": "Juan",
    "edad": 30,
    "ciudad": "Madrid",
    "es_estudiante": False,
    "a": [1, 2, 3, 4]
}   
print(mi_diccionario["nombre"])  # Acceder al valor asociado a la clave "nombre"      